// 方块颜色定义（7种颜色）
COLORREF tetrominoColors[7] = {
    EGERGB(100, 200, 255), // I: 天蓝
    EGERGB(255, 220, 100), // O: 暖黄
    EGERGB(200, 100, 255), // T: 紫
    EGERGB(100, 255, 150), // L: 绿
    EGERGB(255, 100, 100), // J: 红
    EGERGB(255, 180, 100), // S: 橙
    EGERGB(150, 200, 255)  // Z: 浅蓝
};

// 初始化方块
Tetromino createTetromino(TetrominoType type) {
    Tetromino tetromino;
    tetromino.type = type;
    tetromino.x = 3; // 初始位置在棋盘中间
    tetromino.y = 0;
    tetromino.rotation = 0;
    tetromino.color = tetrominoColors[type];
    return tetromino;
}

// 旋转方块
void rotateTetromino(Tetromino *tetromino) {
    tetromino->rotation = (tetromino->rotation + 1) % 4;
}
